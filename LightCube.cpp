void LightCube::render(const glm::mat4& projMat, const glm::mat4& viewMat)
{
	_shader->use();
	_shader->setUniformMatrix("viewMat", viewMat);
	_shader->setUniformMatrix("projMat", projMat);
	_shader->setUniformMatrix("modelMat", _modelMat);

	glBindVertexArray(_vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

void LightCube::setPosition(glm::vec3 pos)
{
	_modelMat = glm::mat4(1.0);
	_modelMat = glm::translate(_modelMat, pos);
	_modelMat = glm::scale(_modelMat, glm::vec3(0.2f));
}